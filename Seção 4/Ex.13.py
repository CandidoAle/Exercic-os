"""
Exercício 13 (29)
Apresentar média simples de 3 numeros
"""

num1 = float(input('Inserir primeiro valor: '))
num2 = float(input('Inserir segundo valor: '))
num3 = float(input('Inserir terceiro valor: '))
num4 = float(input('Inserir quarto valor: '))

resultado = (num1 + num2 + num3 + num4) / 4

print(f'O resultado é {resultado}')